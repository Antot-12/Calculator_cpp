double sum(double num_1, double num_2){
    return num_1 + num_2;
}

double multiplication(double num_1, double num_2){
    return num_1 * num_2;
}

double division (double num_1, double num_2){
    return num_1 / num_2;
}

double pover (double num_1, double num_2){
    return (pow(num_1, num_2));
}

double root (double num_1, double num_2){
    return (pow(num_1, (1/num_2)));
}

double lg (double num_1, double num_2){
    return log(num_1) / log(num_2); 
}

double syn (double num_1){
    return sin(num_1 * M_PI / 180); 
}

double kos (double num_1){
    return cos(num_1 * M_PI / 180); 
}

double tang (double num_1){
    return tan(num_1 * M_PI / 180); 
}

double cot (double num_1){
    return cos(num_1) / sin(num_1); 
}

float okruh (float num1){
    ceil(num1); 
}

